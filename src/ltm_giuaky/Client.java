    private void btnTaoPSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoPSActionPerformed
        check = HamKiemTra.kiemTraSo(txtSoLuong.getText());
        switch (check) {
            case 0 ->
                JOptionPane.showMessageDialog(null, "Bạn vừa nhập không phải số!\nVui lòng nhập lại");
            case -1 ->
                JOptionPane.showMessageDialog(null, "Bạn vừa nhập số nhỏ hơn 0!\nVui lòng nhập lại");
            case 2 ->
                JOptionPane.showMessageDialog(null, "Số lượng không được để trống!\nVui lòng nhập lại");
            case 1 -> {
                try {
                    client.close();
                    client = new Socket(txtAdd.getText(), Integer.parseInt(txtPort.getText()));
                    din = new DataInputStream(client.getInputStream());
                    dout = new DataOutputStream(client.getOutputStream());
                    System.out.println("Ket noi tao phan so thanh cong");
                    txtHienThi.setText("");
                    int sl = Integer.parseInt(txtSoLuong.getText());
                    DSPhanSo = HamPhanSo.DSPhanSoNgauNhien(sl);
                    String hienThi = HamPhanSo.hienThiPhanSo(DSPhanSo);
                    chuoiPhanSo = HamPhanSo.chuyenPhanSoThanhChuoi(DSPhanSo);
                    txtHienThi.setText(hienThi);
                    dout.writeUTF(chuoiPhanSo);
                    System.out.println(chuoiPhanSo);
                } catch (IOException ex) {
                    Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            default -> {
            }
        }

    }//GEN-LAST:event_btnTaoPSActionPerformed

