    private void btnCNNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCNNActionPerformed
        check = HamKiemTra.kiemTraSo(txtPort.getText());
        switch (check) {
            case 0 ->
                JOptionPane.showMessageDialog(null, "Cổng kết nối phải là một số!\nVui lòng nhập lại");
            case -1 ->
                JOptionPane.showMessageDialog(null, "Số hiệu cổng kết nối không được nhỏ hơn 0!\nVui lòng nhập lại");
            case 2 ->
                JOptionPane.showMessageDialog(null, "Số hiệu cổng kết nối không được để trống!\nVui lòng nhập lại");
            case 1 -> {
                try {
                    // TODO add your handling code here:
                    client = new Socket(txtAdd.getText(), Integer.parseInt(txtPort.getText()));
                    din = new DataInputStream(client.getInputStream());
                    dout = new DataOutputStream(client.getOutputStream());
                    System.out.println("Kết nối thành công!");
                    JOptionPane.showMessageDialog(null, "Kết nối thành công");
                    Thread.sleep(1);
                    this.jPanel3.setVisible(true);
                    this.jPanel4.setVisible(true);
                } catch (InterruptedException | IOException ex) {
                    System.out.println("Kết nối không thành công!");
                    JOptionPane.showMessageDialog(null, "Kết nối không thành công");
                    Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            default -> {
            }
        }
    }//GEN-LAST:event_btnCNNActionPerformed

